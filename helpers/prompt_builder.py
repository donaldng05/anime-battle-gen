def build_battle_prompt(your_name, your_traits, friend_name, friend_traits):
    prompt = (
        f"An anime-style battle scene between {your_name}, who has {your_traits}, "
        f"and {friend_name}, who has {friend_traits}. "
        "Glowing energy effects, destroyed city background, dramatic camera angles, cinematic lighting, dust clouds, intense action, ultra-detailed style."
    )
    return prompt
